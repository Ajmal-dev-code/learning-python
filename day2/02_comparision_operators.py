#equal(==)
just_number = 18
if just_number == 18:
    print("no restriction")

#not equal(!=)
if just_number != 18:
    print("not 18")

#greater(>)
if just_number > 18:
    print("can go anywhere")

#less(<)
if just_number < 18:
    print("just a kid")

#greater or equal(>=)
if just_number >= 18:
    print("fine")

#less or equal(<=)
if just_number <= 18:
    print("under aged")